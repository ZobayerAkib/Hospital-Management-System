    private void p_idActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_p_idActionPerformed

        String pId=p_id.getText();

        try{

            Connection connection=ConnectorClass.getcon();
            String dId="select *from Patient where patient_id = '"+pId+"'"  ;
            Statement statement = connection.createStatement();
            ResultSet rs=statement.executeQuery(dId);
            while(rs.next())
            {
                int id= rs.getInt("patient_disease_id");
                String tId="select *from Disease where disease_id = '"+id+"'";
                Statement statement2 = connection.createStatement();
                ResultSet rs2=statement2.executeQuery(tId);
                while(rs2.next())
                {
                    int id2= rs2.getInt("disease_treatment_cost");
                    String cost = Integer.toString(id2);
                    tCost.setText(cost);
                }

            }

            Mcosts.setEditable(false);
            tCost.setEditable(false);
            Totalc.setEditable(false);

        }catch(Exception e)
        {
          
        }
    }//GEN-LAST:event_p_idActionPerformed

