    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        int sum=0;
        int price=Integer.parseInt(MedicineCost.getText());
        int quantity=Integer.parseInt(Quantity.getText());
        int tPrice=Integer.parseInt(tCost.getText());
        int total= (price*quantity);
        DefaultTableModel model=(DefaultTableModel)jTable1.getModel();
        model.addRow(new Object[]{p_id.getText(),Mname.getText(),price,quantity,total});
        for(int i=0; i<jTable1.getRowCount();i++)
        {
            sum=sum+Integer.parseInt(jTable1.getValueAt(i,4).toString());
        }
        Mcosts.setText(String.valueOf(sum));
        int totalAmout=sum+tPrice;
        String TotalAmount =String.valueOf(totalAmout) ;
        Totalc.setText(TotalAmount);

    }//GEN-LAST:event_jButton1ActionPerformed

